#!/usr/bin/env python3
"""Merge Exp01 main table + Exp03 metrics + Exp06 ablation into RESULTS_COMBINED.md"""
from __future__ import annotations

import argparse
import json
from pathlib import Path


def main() -> None:
    p = argparse.ArgumentParser()
    p.add_argument(
        "--exp01_main_table_md",
        default="experiments/exp01_main_qa/results/main_table/main_table.md",
    )
    p.add_argument(
        "--exp01_latest_run_md",
        default="experiments/exp01_main_qa/results/auto_verify_openqa/results.md",
        help="Optional: latest Exp01 single-dataset smoke (openqa); appended after main table if file exists.",
    )
    p.add_argument("--exp03_metrics_json", default="experiments/exp03_retrieval_quality/results/metrics.json")
    p.add_argument("--exp06_ablation_md", default="experiments/exp06_ablation/results/ablation_table.md")
    p.add_argument("--out", default="experiments/RESULTS_COMBINED.md")
    args = p.parse_args()

    parts: list[str] = []
    parts.append("# Combined experiment results (paper tables)\n\n")
    parts.append("Generated by `experiments/combine_experiment_results.py`.\n\n")
    parts.append(
        "**Navigation:**\n\n"
        "- **Main table (HotpotQA + NQ EM):** the first `Experiment 1` section below — from "
        "`experiments/exp01_main_qa/results/main_table/main_table.md`.\n"
        "- **Latest automated verify (single dataset, small N):** scroll to the heading "
        "**`### Experiment 1 — Latest automated verify`** below, or open directly: "
        "`experiments/exp01_main_qa/results/auto_verify_openqa/results.md` "
        "(override with `--exp01_latest_run_md`).\n\n"
        "---\n\n"
    )

    p1 = Path(args.exp01_main_table_md)
    if p1.exists():
        parts.append("## Experiment 1 — Main QA (Hotpot + NQ aggregate)\n\n")
        parts.append(p1.read_text(encoding="utf-8"))
        parts.append("\n\n")
    else:
        parts.append("## Experiment 1 — Main QA (aggregate)\n\n*(file not found: {} )*\n\n".format(p1))

    pv = Path(args.exp01_latest_run_md)
    if pv.exists():
        parts.append("### Experiment 1 — Latest automated verify (single dataset, openqa)\n\n")
        parts.append(pv.read_text(encoding="utf-8"))
        parts.append("\n\n---\n\n")
    else:
        parts.append("---\n\n")

    p3 = Path(args.exp03_metrics_json)
    if p3.exists():
        parts.append("## Experiment 3 — Retrieval Quality\n\n")
        j = json.loads(p3.read_text(encoding="utf-8"))
        parts.append("```json\n")
        parts.append(json.dumps(j, ensure_ascii=False, indent=2))
        parts.append("\n```\n\n---\n\n")
    else:
        parts.append("## Experiment 3 — Retrieval Quality\n\n*(metrics.json not found — run Exp3 script)*\n\n---\n\n")

    p6 = Path(args.exp06_ablation_md)
    if p6.exists():
        parts.append("## Experiment 6 — Ablation\n\n")
        parts.append(p6.read_text(encoding="utf-8"))
        parts.append("\n")
    else:
        parts.append("## Experiment 6 — Ablation\n\n*(ablation_table.md not found — run Exp6 script)*\n")

    out_path = Path(args.out)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text("".join(parts), encoding="utf-8")
    print(f"Wrote {out_path}")


if __name__ == "__main__":
    main()
